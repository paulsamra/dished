import Foundation

extension UIColor
{
    class func dishedColor() -> UIColor
    {
        return UIColor( red: 0, green: 0.55, blue: 0.9, alpha: 1 )
    }
    
    class func commentButtonTextColor() -> UIColor
    {
        return UIColor( red: 0.4, green: 0.41, blue: 0.47, alpha: 1 )
    }
    
    class func greenGradeColor() -> UIColor
    {
        return UIColor( red: 0, green: 0.83, blue: 0.14, alpha: 1 )
    }
    
    class func yellowGradeColor() -> UIColor
    {
        return UIColor( red: 0.96, green: 0.78, blue: 0, alpha: 1 )
    }
    
    class func orangeGradeColor() -> UIColor
    {
        return UIColor( red: 0.93, green: 0.51, blue: 0.16, alpha: 1 )
    }
    
    class func redGradeColor() -> UIColor
    {
        return UIColor( red: 0.92, green: 0, blue: 0, alpha: 1 )
    }
    
    class func unreadNewsColor() -> UIColor
    {
        return UIColor( red: 1, green: 0.93, blue: 0.84, alpha: 1 )
    }
    
    class func followButtonColor() -> UIColor
    {
        return UIColor( red: 0.23, green: 0.8, blue: 0, alpha: 1 )
    }
}